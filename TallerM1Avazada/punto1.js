function hallarDistancia(x,y){
    return Math.sqrt(((Math.pow(Math.pow(x,2)-Math.pow(x,1),2))+(Math.pow(Math.pow(y,2)-Math.pow(y,1)),2)));
}

console.log(hallarDistancia(5,6))

